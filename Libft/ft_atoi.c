#include <stdlib.h>

int	ft_atoi (const char *nptr)
{
	int	a;
	int	b;
	
	a = 0;
	b = 0;
	if (nptr[0] == '-')
		b++;
	while (nptr[b] != '\0')
	{
		a = a * 10 + (nptr[b] - 48);
		b++;
	}
	if (nptr[0] == '-')
		a = a * -1;
	return (a);
}